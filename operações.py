class Operacoes: 
    def soma(x: float, y:float):
        return x+y

    def subtrai(self, x: float, y:float):
        return x-y

    def divide(self, x: float, y:float):
        return x/y

    def multiplica(self, x: float, y:float):
        return x*y

    def potencia(self, x: float, y:float):
        return x**y
    
    def resto(self, x: float, y:float):
        return x%y